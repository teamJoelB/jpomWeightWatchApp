    private void btValiderConnexionActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btValiderConnexionActionPerformed
        // TODO add your handling code here:
        
        User U = new User();
        
        String login = txtID.getText();
        char[] C = txtMDP.getPassword();
        String mdp = new String(C);
        
        
        try {
            U = UserDao.getbyLoginPass(login, mdp);   // une methode dans la class UserDao 
        
            if(U != null){
                Profil P = new Profil();
                this.setVisible(false);
                P.setVisible(true);
            }
        
         else {
                lbMsgConnection.setText("Identifiant ou mot de passe incorrect !!");
            }
        }
        catch (Exception e){
             JOptionPane.showMessageDialog(rootPane, e.getMessage());
        }
        
    }//GEN-LAST:event_btValiderConnexionActionPerformed

